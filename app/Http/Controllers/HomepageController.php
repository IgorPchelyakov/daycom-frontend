    private function getFeedData($feed)
    {
        $encodedFeed = urlencode($feed);
        $LOCAL_NEWS = "http://localhost:4444/api/main-page?feed={$encodedFeed}";
        $SERVER_NEWS = "https://sside.daycom.com.ua/api/main-page?feed={$encodedFeed}";

        $ch = curl_init();
        curl_setopt($ch, CURLOPT_URL, $SERVER_NEWS);
        curl_setopt($ch, CURLOPT_RETURNTRANSFER, 1);
        $response = curl_exec($ch);
        curl_close($ch);

        return json_decode($response, true);
    }
