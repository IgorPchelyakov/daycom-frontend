            <h2>1. Інтеграція нових форматів і підходів</h2>
            <p><b>1.1. Long-reads (довгі статті)</b></p>
            <ul>
                <li>
                    <p><b>Що це? </b>Великі за обсягом матеріали (3 000-6 000 слів), які глибоко занурюються в одну тему або
                        проблему.</p>
                </li>
                <li>
                    <p><b>Як це працює?</b></p>
                    <ul>
                        <li>
                            <p>Фокус на людських історіях, які ілюструють складні політичні чи соціальні процеси.</p>
                        </li>
                        <li>
                            <p>Використання деталізації, описів, метафор і контексту для створення захоплюючого тексту.
                            </p>
                        </li>
                        <li>
                            <p>Приклад теми: "Рік у житті волонтера на фронті" або "Як одна громада відновила своє місто
                                після війни".
                            </p>
                        </li>
                    </ul>
                </li>
            </ul>
            <p><b>1.2. Тематичні спецпроєкти, які ми називаємо “Поєднані теми”</b></p>
            <ul>
                <li>
                    <p><b>Що це? </b>Серія статей, інтерв’ю, фоторепортажів та відео, об’єднаних однією темою.</p>
                </li>
                <li>
                    <p><b>Приклади тем:</b></p>
                    <ul>
                        <li>
                            <p>"Життя в тіні війни": історії цивільних, які залишилися в гарячих точках.</p>
                        </li>
                        <li>
                            <p>"Українська діаспора": як світ підтримує Україну.</p>
                        </li>
                        <li>
                            <p>"Відновлення інфраструктури": як Україна будує своє майбутнє після руйнувань.
                            </p>
                        </li>
                    </ul>
                </li>
            </ul>
            <p><b>1.3. Live Coverage (пряма трансляція подій)</b></p>
            <ul>
                <li>
                    <p><b>Що це? </b>Текстова чи відеотрансляція великих подій у реальному часі.</p>
                </li>
                <li>
                    <p><b>Як це використовувати?</b></p>
                    <ul>
                        <li>
                            <p>Оперативне висвітлення подій, таких як політичні переговори, засідання міжнародних
                                організацій, військові зведення чи культурні заходи.</p>
                        </li>
                        <li>
                            <p>Приклад: "Хроніка саміту НАТО: ключові рішення для України".</p>
                        </li>
                    </ul>
                </li>
            </ul>
        </div>
        <div class="mb-3 border-bottom">
            <h2>2. Розвиток narrative journalism</h2>
            <p><b>2.1. Розширення кількості історій через особистий досвід</b></p>
            <ul>
                <li>
                    <p>Включення не лише відомих особистостей, але й "невідомих героїв":</p>
                    <ul>
                        <li>
                            <p>Волонтери, які допомагають переселенцям.</p>
                        </li>
                        <li>
                            <p>Підприємці, які відновлюють свій бізнес під час війни.</p>
                        </li>
                        <li>
                            <p>Родини, які повернулися у звільнені міста.</p>
                        </li>
                    </ul>
                </li>
                <li>
                    <p><b>Приклад: </b>"Як фермер із Харківщини перетворив поле війни на новий врожай".</p>
                </li>
            </ul>
            <p><b>2.2. Дослідження впливу подій на повсякденне життя</b></p>
            <ul>
                <li>
                    <p>Глибокі розповіді про те, як великі події (війна, реформи, економічні зміни) змінюють життя окремих
                        людей.</p>
                </li>
                <li>
                    <p><b>Приклад: </b>"Життя після вимушеної міграції: як українські сім’ї будують нове життя в Європі".
                    </p>
                </li>
            </ul>
