            <h2 style="font-size: 30px; line-height: 36px; font-weight: 300;">Газета Дейком та Narrative Journalism</h2>
            <p><b>Газета "Дейком"</b> намагається повністю інтегрувати досвід роботи із <b>"narrative journalism"</b> і
                поглибити свої підходи до створення контенту. Ці пропозиції зосереджені на посиленні стилю, формування нових
